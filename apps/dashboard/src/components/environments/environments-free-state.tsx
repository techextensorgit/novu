export function FreeTierState() {
  const track = useTelemetry();
  const navigate = useNavigate();
  const { currentOrganization } = useAuth();
  const { environments = [] } = useFetchEnvironments({
    organizationId: currentOrganization?._id,
  });
  const { currentEnvironment } = useEnvironment();

  return (
    <div className="flex h-full w-full flex-col items-center justify-center gap-6 px-4">
      <div className="flex w-full max-w-[480px] flex-col items-center gap-6 text-center">
        <div className="flex w-full flex-col gap-3">
          <div className="flex flex-col items-center gap-2">
            <div className="mb-[50px]">
              <EmptyStateSvg />
            </div>
            <h2 className="text-foreground-900 text-label-md">Manage Your Environments</h2>
            <p className="text-text-soft text-label-xs mb-3 max-w-[300px]">
              Create additional environments to test, stage, or experiment without affecting your live systems.
            </p>
          </div>
          <Separator variant="line-text">YOUR ENVIRONMENTS</Separator>
          <div className="flex w-full items-center justify-center px-5">
            <Table>
              <TableHeader className="w-full">
                <TableRow>
                  <TableHead>Name</TableHead>
                  <TableHead>Identifier</TableHead>
                </TableRow>
              </TableHeader>
              <TableBody>
                {environments.map((environment) => (
                  <TableRow key={environment._id} className="group relative isolate">
                    <TableCell className="font-medium">
                      <div className="flex items-center gap-2">
                        <EnvironmentBranchIcon size="sm" environment={environment} />
                        <div className="flex items-center gap-1">
                          <TruncatedText className="max-w-[32ch]">{environment.name}</TruncatedText>
                          {environment._id === currentEnvironment?._id && (
                            <Badge color="blue" size="sm" variant="lighter">
                              Current
                            </Badge>
                          )}
                        </div>
                      </div>
                    </TableCell>
                    <TableCell>
                      <div className="flex items-center gap-1 transition-opacity duration-200">
                        <TruncatedText className="text-foreground-400 font-code block text-xs">
                          {environment.identifier}
                        </TruncatedText>
                        <CopyButton
                          className="z-10 flex size-2 p-0 px-1 opacity-0 group-hover:opacity-100"
                          valueToCopy={environment.identifier}
                          size="2xs"
                          mode="ghost"
                        />
                      </div>
                    </TableCell>
                  </TableRow>
                ))}
              </TableBody>
            </Table>
          </div>
        </div>

        <div>
          <Button
            variant="primary"
            mode="gradient"
            size="xs"
            className="mb-3.5"
            onClick={() => {
              track(TelemetryEvent.UPGRADE_TO_BUSINESS_TIER_CLICK, {
                source: 'environments-page',
              });
              navigate(ROUTES.SETTINGS_BILLING);
            }}
            leadingIcon={RiSparkling2Line}
          >
            Upgrade to Business Tier
          </Button>
          <Link to={'https://docs.novu.co/concepts/environments'} target="_blank">
            <LinkButton size="sm" leadingIcon={RiBookMarkedLine}>
              How does this help?
            </LinkButton>
          </Link>
        </div>
      </div>
    </div>
  );
}

